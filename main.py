def complex_add(a, b):
    """Add two complex numbers."""
    return (a[0] + b[0], a[1] + b[1])  # (real, imaginary)

def complex_sub(a, b):
    """Subtract two complex numbers."""
    return (a[0] - b[0], a[1] - b[1])  # (real, imaginary)

